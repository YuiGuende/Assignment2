package model;

/**
 *
 * @author LEGION
 */
public class Node<T extends Comparable<T>> {

        
    public T data;
    private int height;
    public Node<T> left;
    public Node<T> right;


    public Node(T data) {
        this.data = data;
        this.height = 1;
        this.left = null;
        this.right = null;
    }

    public T getData() {
        return data;
    }

    public void setData(T data) {
        this.data = data;
    }

    public int getHeight() {
        return height;
    }

    public void setHeight(int height) {
        this.height = height;
    }

    public Node<T> getLeft() {
        return left;
    }

    public void setLeft(Node<T> left) {
        this.left = left;
    }

    public Node<T> getRight() {
        return right;
    }

    public void setRight(Node<T> right) {
        this.right = right;
    }

}
